package com.crime.entity;

public class PoliceInjDeathsPerYear {

	private String name;
	private int noInjDeaths;
	private int year;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNoInjDeaths() {
		return noInjDeaths;
	}
	public void setNoInjDeaths(int noInjDeaths) {
		this.noInjDeaths = noInjDeaths;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
}
