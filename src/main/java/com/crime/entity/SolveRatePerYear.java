package com.crime.entity;

public class SolveRatePerYear {

	private String name;
	private int solveRate;
	private int year;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSolveRate() {
		return solveRate;
	}
	public void setSolveRate(int solveRate) {
		this.solveRate = solveRate;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
}
