package com.crime.entity;

public class CrimeAgainstChildPerYear {

	private String name;
	private int crimeChild;
	private int year;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCrimeChild() {
		return crimeChild;
	}
	public void setCrimeChild(int crimeChild) {
		this.crimeChild = crimeChild;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	
	
}
