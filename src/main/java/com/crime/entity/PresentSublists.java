package com.crime.entity;

public class PresentSublists {
	private String name;
	private int crimes;
	private int casualties;
	private int retirement;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCrimes() {
		return crimes;
	}
	public void setCrimes(int crimes) {
		this.crimes = crimes;
	}
	public int getCasualties() {
		return casualties;
	}
	public void setCasualties(int casualties) {
		this.casualties = casualties;
	}
	public int getRetirement() {
		return retirement;
	}
	public void setRetirement(int retirement) {
		this.retirement = retirement;
	}
}
