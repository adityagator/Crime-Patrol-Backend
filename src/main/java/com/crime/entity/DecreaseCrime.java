package com.crime.entity;

public class DecreaseCrime {
	private String name;
	private int ffy;
	private int lfy;
	private double percentDecrease;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getFfy() {
		return ffy;
	}
	public void setFfy(int ffy) {
		this.ffy = ffy;
	}
	public int getLfy() {
		return lfy;
	}
	public void setLfy(int lfy) {
		this.lfy = lfy;
	}
	public double getPercentDecrease() {
		return percentDecrease;
	}
	public void setPercentDecrease(double percentDecrease) {
		this.percentDecrease = percentDecrease;
	}
}
